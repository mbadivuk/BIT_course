var uslov = 2;
var finalResult = ""

switch (uslov) {
    case 1:
        finalResult = "Rezultat je 1";
        console.log(finalResult);
        break;
    case 2:
        finalResult = "Rezultat je 2"
        console.log(finalResult);
        break;
    case "Marko":
        finalResult = "Rezultat je Marko"
        console.log(finalResult);
        break;
    default:
        finalResult = "Nema rezultata"
        console.log(finalResult);
}